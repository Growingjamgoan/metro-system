package com.sapient.metrosystem.exception;

public class MinimumBalanceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7075574841062904667L;

	public MinimumBalanceException() {
		super();

	}

	public MinimumBalanceException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public MinimumBalanceException(String arg0) {
		super(arg0);

	}

	public MinimumBalanceException(Throwable arg0) {
		super(arg0);

	}

}
